#[test]
fn test_get_slice_qp_y_p_frame() {
    let buffer: &[u8] = &[
        // slice (P-frame)
        0x00, 0x00, 0x00, 0x01, 0x02, 0x01, 0xd0, 0x0f, 0xe4, 0x16, 0x80, 0xf4, 0x5a, 0xb4, 0x85,
        0x6b, 0x17, 0xaa, 0xc1, 0x94, 0xa8, 0x9f, 0x32, 0x11, 0xe4, 0x44, 0xa5, 0xfd, 0xe7, 0x80,
        0xda, 0xea, 0x21, 0x4c, 0x08, 0x23, 0xea, 0x58, 0x15, 0xa3, 0x4c, 0x1a, 0xb3, 0x80, 0x9b,
        0x63, 0x50, 0x11, 0x75, 0x9a, 0xcc, 0x06, 0x09, 0x69, 0x97, 0x75, 0xa0, 0x02, 0x24, 0x22,
        0x1c, 0x06, 0xa5, 0x69, 0x6e, 0xba, 0x9c, 0x79, 0x58, 0x1e, 0x52, 0xa8, 0x26, 0xfe, 0x98,
        0x6f, 0x65, 0xee, 0x57, 0x10, 0x4f, 0x67, 0xe8, 0x43, 0xde, 0x8e, 0xe6, 0x40, 0x28, 0x36,
        0x45, 0x06, 0x5e, 0xe8, 0x80, 0x34, 0xc0, 0x06, 0xf2, 0x16, 0x4b, 0x78, 0x5f, 0x98, 0x56,
        0xcc, 0xd9, 0x59, 0x7a, 0xf3, 0x30, 0x5d, 0xa9, 0xc7, 0x84, 0x4a, 0xe0, 0x16, 0xbf, 0x07,
        0x24, 0x32, 0x65, 0xbd, 0x39, 0xe2, 0x30, 0xbf, 0x27, 0xd3, 0x61, 0x25, 0x02, 0xae, 0x5a,
        0xa1, 0x08, 0x9b, 0x90, 0x14, 0x2a, 0x09, 0xd1, 0x4a,
    ];

    let mut state = h265nal_sys::BitstreamParserState::new().expect("state create failed");

    // Seed the state as per C++ test
    state.seed_vps(0).expect("seed vps failed");
    state
        .seed_sps(0, 1, 1, 0, 0, 0, 0)
        .expect("seed sps failed");
    state.seed_pps(0).expect("seed pps failed");

    let qp_y =
        h265nal_sys::utils_get_slice_qp_y(buffer, &mut state).expect("get slice qp y failed");

    assert_eq!(qp_y.len(), 1);
    assert_eq!(qp_y[0], 26);
}
