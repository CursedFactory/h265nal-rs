#[test]
fn test_get_slice_qp_y_iframe() {
    let buffer: &[u8] = &[
        // slice (I-frame)
        0x00, 0x00, 0x00, 0x01, 0x26, 0x01, 0xaf, 0x09, 0x40, 0xfb, 0x27, 0x5b, 0xfd, 0x5d, 0xc2,
        0x4d, 0xe8, 0xb2, 0x0f, 0xcd, 0x68, 0xdb, 0xc3, 0x41, 0x12, 0x2e, 0x13, 0x8d, 0xdf, 0x66,
        0xc9, 0x1f, 0xaa, 0xd4, 0x9b, 0x8d, 0xdd, 0xe2, 0xa1, 0xda, 0x2e, 0xbd, 0x53, 0x74, 0xd1,
        0xbb, 0xde, 0x54, 0x8f, 0xa5, 0xe7, 0x2f, 0xcc, 0xf7, 0x98, 0xd6, 0x33, 0xd5, 0x06, 0x01,
        0x52, 0x84, 0xbc, 0xa7, 0xe6, 0x02, 0x7f, 0xe9, 0x50, 0x0a, 0x9a, 0x60, 0x89, 0xa0, 0xc0,
        0xb4, 0x6d, 0x60, 0x53, 0xe5, 0xdd, 0x93, 0xde, 0x03, 0xff, 0xa8, 0xb0, 0x4d, 0x27, 0xa5,
        0x82, 0xba, 0xac, 0x63, 0x8b, 0x6f, 0x69, 0x7f, 0x93, 0xb2, 0xe3, 0x0c, 0xfd, 0x29, 0x44,
        0x42, 0xa7, 0x13, 0xe9, 0xec, 0x37, 0xbb, 0x93, 0xe0, 0x62, 0xa9, 0xa4, 0x44, 0x45, 0x59,
        0x16, 0xf6, 0xb6, 0x5b, 0x3a, 0xdb, 0xc3,
    ];

    let mut state = h265nal_sys::BitstreamParserState::new().expect("state create failed");

    // Seed the parser state as done in the C++ test
    state.seed_vps(0).expect("seed vps failed");
    state
        .seed_sps(
            0, // sps_id
            1, // sample_adaptive_offset_enabled_flag
            1, // chroma_format_idc
            0, // log2_min_luma_coding_block_size_minus3
            0, // log2_diff_max_min_luma_coding_block_size
            0, // pic_width_in_luma_samples
            0, // pic_height_in_luma_samples
        )
        .expect("seed sps failed");
    state.seed_pps(0).expect("seed pps failed");

    let qp_y =
        h265nal_sys::utils_get_slice_qp_y(buffer, &mut state).expect("get slice qp y failed");

    assert_eq!(qp_y.len(), 1);
    assert_eq!(qp_y[0], 35);
}
