#[test]
fn test_sample_bitstream_alt() {
    let buffer = [
        0x00, 0x00, 0x00, 0x01, 0x40, 0x01, 0x0c, 0x01, 0xff, 0xff, 0x01, 0x60, 0x00, 0x00, 0x03,
        0x00, 0xb0, 0x00, 0x00, 0x03, 0x00, 0x00, 0x03, 0x00, 0x5d, 0xac, 0x59, 0x00, 0x00, 0x00,
        0x01, 0x42, 0x01, 0x01, 0x01, 0x60, 0x00, 0x00, 0x03, 0x00, 0xb0, 0x00, 0x00, 0x03, 0x00,
        0x00, 0x03, 0x00, 0x5d, 0xa0, 0x02, 0x80, 0x80, 0x2e, 0x1f, 0x13, 0x96, 0xbb, 0x93, 0x24,
        0xbb, 0x95, 0x82, 0x83, 0x03, 0x01, 0x76, 0x85, 0x09, 0x40, 0x00, 0x00, 0x00, 0x01, 0x44,
        0x01, 0xc0, 0xf3, 0xc0, 0x02, 0x10, 0x00, 0x00, 0x00, 0x01, 0x26, 0x01, 0xaf, 0x09, 0x40,
        0xf3, 0xb8, 0xd5, 0x39, 0xba, 0x1f, 0xe4, 0xa6, 0x08, 0x5c, 0x6e, 0xb1, 0x8f, 0x00, 0x38,
        0xf1, 0xa6, 0xfc, 0xf1, 0x40, 0x04, 0x3a, 0x86, 0xcb, 0x90, 0x74, 0xce, 0xf0, 0x46, 0x61,
        0x93, 0x72, 0xd6, 0xfc, 0x35, 0xe3, 0xc5, 0x6f, 0x0a, 0xc4, 0x9e, 0x27, 0xc4, 0xdb, 0xe3,
        0xfb, 0x38, 0x98, 0xd0, 0x8b, 0xd5, 0xb9, 0xb9, 0x15, 0xb4, 0x92, 0x49, 0x97, 0xe5, 0x3d,
        0x36, 0x4d, 0x45, 0x32, 0x5c, 0xe6, 0x89, 0x53, 0x76, 0xce, 0xbb, 0x83, 0xa1, 0x27, 0x35,
        0xfb, 0xf3, 0xc7, 0xd4, 0x85, 0x32, 0x37, 0x94, 0x09, 0xec, 0x10,
    ];

    let nal_units =
        h265nal_sys::bitstream_parse(&buffer, None, true).expect("ParseBitstream failed");

    assert_eq!(nal_units.len(), 4);
    assert_eq!(nal_units[0].offset, 4);
    assert_eq!(nal_units[0].length, 0x17);
    assert_eq!(nal_units[0].parsed_length, 20);
    assert_eq!(nal_units[0].checksum, [0xbf, 0xa2, 0x45, 0x94]);

    assert_eq!(nal_units[1].offset, 31);
    assert_eq!(nal_units[1].length, 0x27);
    assert_eq!(nal_units[1].parsed_length, 36);
    assert_eq!(nal_units[1].checksum, [0x00, 0x3d, 0xc2, 0x9d]);

    assert_eq!(nal_units[2].offset, 74);
    assert_eq!(nal_units[2].length, 0x07);
    assert_eq!(nal_units[2].parsed_length, 7);
    assert_eq!(nal_units[2].checksum, [0xfb, 0xfc, 0x2f, 0x0b]);

    assert_eq!(nal_units[3].offset, 85);
    assert_eq!(nal_units[3].length, 0x5b);
    assert_eq!(nal_units[3].parsed_length, 5);
    assert_eq!(nal_units[3].checksum, [0x6c, 0x88, 0x0c, 0xe4]);
}
