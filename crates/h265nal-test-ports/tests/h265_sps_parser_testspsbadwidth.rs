#[test]
fn test_sps_bad_width() {
    // SPS for a 1926x736 camera capture (1926 does not divide 8)
    let buffer = [
        0x01, 0x01, 0x60, 0x00, 0x00, 0x03, 0x00, 0xb0, 0x00, 0x00, 0x03, 0x00, 0x00, 0x03, 0x00,
        0x5d, 0xa1, 0x02, 0x80, 0x80, 0x2e, 0x1f, 0x13, 0x96, 0xbb, 0x93, 0x24, 0xbb, 0x95, 0x82,
        0x83, 0x03, 0x01, 0x76, 0x85, 0x09, 0x40,
    ];
    let result = h265nal_sys::sps_parse(&buffer);
    assert_eq!(result, Err(h265nal_sys::Error::ParseFailure));
}
